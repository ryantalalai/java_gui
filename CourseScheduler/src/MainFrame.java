    private void scheduleCoursesSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleCoursesSubmitButtonActionPerformed
        String courseCode = selectCourseComboBox.getSelectedItem().toString();
        String studentName = selectStudentComboBox.getSelectedItem().toString();
        String studentID = nameToStudentID(studentName);
        int numberOfStudentsScheduled = ScheduleQueries.getScheduledStudentCount(currentSemester, courseCode);

        int numberOfSeats = CourseQueries.getCourseSeats(currentSemester, courseCode);

        if (numberOfSeats > numberOfStudentsScheduled){
            ScheduleEntry schedule = new ScheduleEntry(currentSemester, courseCode, studentID, "S");
            ScheduleQueries.addScheduleEntry(schedule);
            scheduleCoursesStatusLabel.setText(studentName + " has been scheduled for " + courseCode);
        }
        else {
            ScheduleEntry schedule = new ScheduleEntry(currentSemester, courseCode, studentID, "W");
            ScheduleQueries.addScheduleEntry(schedule);
            scheduleCoursesStatusLabel.setText(studentName + " has been waitlisted for " + courseCode);
        }
    }//GEN-LAST:event_scheduleCoursesSubmitButtonActionPerformed

