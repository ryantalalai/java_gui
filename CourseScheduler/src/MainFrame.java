    public void rebuildSemesterComboBoxes()
    {
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        currentSemesterComboBox.setModel(new javax.swing.DefaultComboBoxModel(semesters.toArray()));
        if (semesters.size() > 0)
        {
            currentSemesterLabel.setText(semesters.get(0));
            currentSemester = semesters.get(0);
        }
        else
        {
            currentSemesterLabel.setText("None, add a semester.");
            currentSemester = "None";
        }
    }
    
    public void rebuildCourseComboBoxes()
    {
        ArrayList<CourseEntry> courses = CourseQueries.getAllCourses(currentSemester);
        ArrayList<String> courseCodes = new ArrayList<String>();
        for (CourseEntry course:courses)
       {
           String code = course.getCourseCode();
           courseCodes.add(code);  
       }
        selectCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(courseCodes.toArray()));
        displayCourseListComboBox.setModel(new javax.swing.DefaultComboBoxModel(courseCodes.toArray()));
        dropCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(courseCodes.toArray()));
        studentDropCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(courseCodes.toArray()));

    }
    
    public void rebuildStudentComboBoxes()
    {
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        ArrayList<String> studentNames = new ArrayList<String>();
        for (StudentEntry student:students)
       {
           String fullName = student.getLastName() + ", " + student.getFirstName();
           studentNames.add(fullName);  
       }
        selectStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(studentNames.toArray()));
        displayScheduleStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(studentNames.toArray()));
        dropStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(studentNames.toArray()));
        studentDropCourseSelectStudentComboBox.setModel(new javax.swing.DefaultComboBoxModel(studentNames.toArray()));

    }
    
    public static String nameToStudentID(String studentName)
    {
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        String[] separatedName = studentName.split(", ");
        String studentID = "";
        
        for (StudentEntry student:students)
       {
           if (separatedName[0].equals(student.getLastName()) && separatedName[1].equals(student.getFirstName())){
               studentID  = student.getStudentID();
           }  
       }
        return studentID;
    }

