    private void dropStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropStudentButtonActionPerformed
        String student = dropStudentComboBox.getSelectedItem().toString();
        String studentID = nameToStudentID(student);
        StudentQueries.dropStudent(studentID);
        rebuildStudentComboBoxes();
        dropStudentTextArea.setText(student + " " + studentID + " has been dropped from the list of students.\n");
        
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        for (String semester:semesters)
        {
            dropStudentTextArea.append("\nFor Semester: " + semester + "\n");
            ArrayList<ScheduleEntry> studentSchedule = ScheduleQueries.getScheduleByStudent(semester, studentID);
            for (ScheduleEntry course:studentSchedule)
            {   
                if ("S".equals(course.getStatus())) {
                ScheduleQueries.dropStudentScheduleByCourse(semester, studentID, course.getCourseCode());
                ArrayList<ScheduleEntry> waitlistedStudents = ScheduleQueries.getWaitlistedStudentsByCourse(semester, course.getCourseCode());
                    if (waitlistedStudents.isEmpty()) {
                        dropStudentTextArea.append(student + " " + studentID + " has been dropped from " + course.getCourseCode() + "\n");
                    }
                    else{
                        ScheduleQueries.updateScheduleEntryStatus(semester, waitlistedStudents.get(0));
                        StudentEntry newStudentName = StudentQueries.getStudent(waitlistedStudents.get(0).getStudentID());
                        dropStudentTextArea.append(student + " " + studentID + " has been dropped from " + course.getCourseCode() + "\n");
                        dropStudentTextArea.append(newStudentName.getLastName() + ", " + newStudentName.getFirstName() + " " + 
                                newStudentName.getStudentID() + " has been scheduled into " + course.getCourseCode() + "\n");
                    }
                }
                if ("W".equals(course.getStatus())) {
                    ScheduleQueries.dropStudentScheduleByCourse(semester, studentID, course.getCourseCode());
                    dropStudentTextArea.append(student + " " + studentID + " has been dropped from the waitlist for " + course.getCourseCode() + "\n");
                }
            }
        }
            
        
    }//GEN-LAST:event_dropStudentButtonActionPerformed

    private void dropCourseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropCourseButtonActionPerformed
        String courseCode = dropCourseComboBox.getSelectedItem().toString();
        ArrayList<ScheduleEntry> scheduledStudents = ScheduleQueries.getScheduledStudentsByCourse(currentSemester, courseCode);
        ArrayList<String> scheduledStudentsDropped = new ArrayList();
        ArrayList<String> waitlistedStudentsDropped = new ArrayList();
        
        for (ScheduleEntry student:scheduledStudents){
            if ("S".equals(student.getStatus())) {
                scheduledStudentsDropped.add(student.getStudentID());
            }
            else {
                waitlistedStudentsDropped.add(student.getStudentID());
            }
        }
        
        CourseQueries.dropCourse(currentSemester, courseCode);
        ScheduleQueries.dropScheduleByCourse(currentSemester, courseCode);
        rebuildCourseComboBoxes();
        dropCourseTextArea.setText("Scheduled students dropped from " + courseCode + ":\n");
        
        for (String student:scheduledStudentsDropped) {
            StudentEntry studentEntry = StudentQueries.getStudent(student);
            dropCourseTextArea.append(studentEntry.getLastName() + ", " + studentEntry.getFirstName() + " " + studentEntry.getStudentID() + "\n");
        }
        
        dropCourseTextArea.append("\nWaitlisted students dropped from " + courseCode + ":\n");
        
        for (String student:waitlistedStudentsDropped) {
            StudentEntry studentEntry = StudentQueries.getStudent(student);
            dropCourseTextArea.append(studentEntry.getLastName() + ", " + studentEntry.getFirstName() + " " + studentEntry.getStudentID() + "\n");
        }
        
        
        
    }//GEN-LAST:event_dropCourseButtonActionPerformed

